    <li><h2>THE DOMINIQUE OUATTARA HIGHSCHOOL IN KONG</h2></li>

One of the main actions of Children of Africa Foundation in the field of education is surely the construction of a highschool for young girls in Kong.

In February 2000, Children of Africa Foundation officially handed over the keys of the school to the city of Kong. Originally created to accommodate young girls from the northern region of Côte d'Ivoire to promote girls’ education, the Dominique Ouattara highschool for young girls will finally become a mixed school.
In 2012, the school will be completely renovated and will see its capacity strengthened. It becomes the Dominique Ouattara Highschool of Kong.

 

 
<li><h2>LA CASE DES ENFANTS</h2></li>

The idea behind the opening of a shelter for Children dates back to September 1994. A group of friends, eager to help children living on the streets, decided to open a center to receive them. Then in 1998, faced with management difficulties, they decided to hand the center over to Mrs. Dominique Ouattara to promote her humanitarian action in Côte d'Ivoire.
In almost a quarter century, several generations of children have been saved from the streets. Today, thanks to the supervisors, these children have received a good education and represent a source of pride for Mrs. Dominique Ouattara, President and Founder of Children of Africa, and for their families.

 

 
<li><h2>MOBILE LIBRARIES</h2></li>

Children of Africa Foundation has nine Bibliobuses that crisscross all the regions of the country to introduce children to reading skills. The Libraries are equipped with thousands of books of all kinds, computers connected to Internet, to enable children to learn computer and a projector to watch movies and learn.

 

 
<li><h2>HEALTH CARAVAN</h2></li>

Children of Africa Foundation has since 1998 organized several health caravans that have over the years strengthened its prestige among the poor. In fact, the medical team of Children Of Africa traveled all over the country from 1998 to 2017. The aim was to contribute to the improvement of children’s health and to protect them against pathological endemics such as typhoid fever and meningitis. During the various health caravans conducted by the Foundation, thousands of children were dewormed against intestinal worms.

 

 
<li><h2>THE DOMINIQUE OUATTARA MOTHER AND CHILD’S HOSPITAL OF BINGERVILLE</h2></li>

The Bingerville Mother and Child’s Hospital (MCH) is one of the major projects of Children of Africa Foundation. This important project that was conceived and realized by Mrs. Dominique Ouattara, President and Founder of Children Of Africa, is to provide an adequate response to the difficulties encountered by children and mothers concerning their healthcare. This hospital is equipped with the latest medical technologies and a quality staff rigorously selected based on  their skills. It opened its doors to the people in March 2018 during the 20th anniversary of the Foundation.

 

 
<li><h2>CENTERS FOR CHILDREN INVOLVED INTO TRAFFICKING, EXPLOITATION AND CHILD LABOUR></h2></li>

At the forefront of the fight against trafficking, exploitation and child labour, Mrs. Dominique Ouattara has undertaken the construction of three reception centers for children victims of these phenomena.
Three localities in Côte d'Ivoire have been selected to house these centers. These are Soubré, Bouaké and Ferkessédougou. The center of Soubré is completed. It was inaugurated on June 7, 2018, and the other two are under construction.

